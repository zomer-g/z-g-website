/**
 * Manifest + bootstrap for browser-extension support pages.
 *
 * The five-route series under /ocal and /ocoi-extension lives in the Page
 * table so it can be edited and toggled (publish/hide) from the admin UI.
 * Rather than make the deploy depend on a manual seed step, we keep the
 * default Hebrew content here and call ensureExtensionPagesExist() from
 * /api/pages GET — so the rows materialise automatically the first time
 * the admin opens the pages list, with status=DRAFT (hidden from the
 * public until explicitly published).
 *
 * Re-running ensure is a no-op for slugs that already exist; it never
 * overwrites admin-edited content or flips status.
 */

/* ── TipTap helpers ── */
function t(text: string) {
  return { type: "text", text };
}
function tb(text: string) {
  return { type: "text", text, marks: [{ type: "bold" }] };
}
function tl(text: string, href: string) {
  return { type: "text", text, marks: [{ type: "link", attrs: { href } }] };
}
function h2(text: string) {
  return { type: "heading", attrs: { level: 2 }, content: [t(text)] };
}
function p(...nodes: unknown[]) {
  return { type: "paragraph", content: nodes };
}
function li(...nodes: unknown[]) {
  return { type: "listItem", content: [p(...nodes)] };
}
function ul(...items: unknown[]) {
  return { type: "bulletList", content: items };
}
function hr() {
  return { type: "horizontalRule" };
}

