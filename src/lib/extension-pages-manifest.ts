/**
 * Idempotently materialise extension-page rows in the Page table.
 *
 * Called from /api/pages GET so the rows appear automatically in the
 * admin UI on first deploy — no manual seed step needed.
 *
 * - If a slug is missing: insert as DRAFT (hidden from public until
 *   the user clicks "מפורסם" in the admin).
 * - If a slug already exists: leave it completely alone. We do NOT
 *   refresh content here — once an admin has edited a page we must
 *   never overwrite their work, and we don't flip status back to DRAFT.
 *
 * Failures are swallowed so a transient DB hiccup never breaks the
 * admin pages list.
 */
export async function ensureExtensionPagesExist(): Promise<void> {
  try {
    const slugs = EXTENSION_PAGES_MANIFEST.map((e) => e.slug);
    const existing = await prisma.page.findMany({
      where: { slug: { in: slugs } },
      select: { slug: true },
    });
    const have = new Set(existing.map((e) => e.slug));

    const missing = EXTENSION_PAGES_MANIFEST.filter((e) => !have.has(e.slug));
    if (missing.length === 0) return;

    await prisma.$transaction(
      missing.map((entry) =>
        prisma.page.create({
          data: {
            slug: entry.slug,
            title: entry.title,
            // Prisma's Json input type is structurally compatible with the
            // TipTap doc shape, but TS can't see that through the helper
            // factories — cast at the boundary, value is plain JSON.
            content: entry.build() as never,
            status: "DRAFT",
          },
        }),
      ),
    );
  } catch (error) {
    console.error("ensureExtensionPagesExist failed:", error);
  }
}
