async function main() {
  // Pick an author: prefer the site owner, then any ADMIN, then the first user.
  const author =
    (await prisma.user.findUnique({ where: { email: "zomerg@gmail.com" } })) ??
    (await prisma.user.findFirst({ where: { role: "ADMIN" } })) ??
    (await prisma.user.findFirst());

  if (!author) throw new Error("No user found to attribute the post to.");

  const now = new Date();

  const post = await prisma.plilistPost.upsert({
    where: { slug: SLUG },
    update: {
      title: TITLE,
      content,
      excerpt: EXCERPT,
      status: "PUBLISHED",
      publishedAt: now,
      seoDesc: EXCERPT.slice(0, 300),
    },
    create: {
      slug: SLUG,
      title: TITLE,
      content,
      excerpt: EXCERPT,
      status: "PUBLISHED",
      publishedAt: now,
      authorId: author.id,
      seoTitle: TITLE,
      seoDesc: EXCERPT.slice(0, 300),
      tags: ["מאגרי פסיקה", "מחקר משפטי", "טכנולוגיה משפטית"],
    },
  });

  console.log("Published plilist post:");
  console.log("  id:        ", post.id);
  console.log("  slug:      ", post.slug);
  console.log("  status:    ", post.status);
  console.log("  author:    ", author.email);
  console.log("  url:        /haplilist/" + post.slug);
  await prisma.$disconnect();
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
